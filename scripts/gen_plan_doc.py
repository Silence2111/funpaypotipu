def shade(cell, hexfill):
    tcPr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd"); shd.set(qn("w:val"), "clear"); shd.set(qn("w:color"), "auto"); shd.set(qn("w:fill"), hexfill)
    tcPr.append(shd)

def setcell(cell, text, bold=False, white=False, size=9.5, color=None):
    cell.text = ""; p = cell.paragraphs[0]; r = p.add_run(str(text)); r.bold = bold; r.font.size = Pt(size)
    if white: r.font.color.rgb = RGBColor(0xFF,0xFF,0xFF)
    elif color: r.font.color.rgb = color

def table(headers, rows, widths=None, headfill="1F3864"):
    t = doc.add_table(rows=1, cols=len(headers)); t.style = "Table Grid"
    for i,h in enumerate(headers):
        setcell(t.rows[0].cells[i], h, bold=True, white=True); shade(t.rows[0].cells[i], headfill)
    for row in rows:
        cells = t.add_row().cells
        for i,v in enumerate(row): setcell(cells[i], v)
    if widths:
        for i,w in enumerate(widths):
            for r in t.rows: r.cells[i].width = Inches(w)
    doc.add_paragraph(); return t

def title(text):
    p = doc.add_paragraph(); r = p.add_run(text); r.bold = True; r.font.size = Pt(22); r.font.color.rgb = NAVY
def h1(text):
    p = doc.add_paragraph(); p.paragraph_format.space_before = Pt(14); r = p.add_run(text); r.bold = True; r.font.size = Pt(15); r.font.color.rgb = NAVY
def h2(text):
    p = doc.add_paragraph(); p.paragraph_format.space_before = Pt(8); r = p.add_run(text); r.bold = True; r.font.size = Pt(12.5); r.font.color.rgb = RGBColor(0x2A,0x2A,0x2A)
def para(text, italic=False, color=None, size=10.5):
    p = doc.add_paragraph(); r = p.add_run(text); r.italic = italic; r.font.size = Pt(size)
    if color: r.font.color.rgb = color
    return p
def bullet(text):
    doc.add_paragraph(style="List Bullet").add_run(text)
def kv(label, value):
    p = doc.add_paragraph(); r = p.add_run(label + ": "); r.bold = True; p.add_run(value)

